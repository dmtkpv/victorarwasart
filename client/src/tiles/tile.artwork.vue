        .image {
            position: relative;
            cursor: crosshair;
            img {
                @extend %u-stretch;
                position: absolute;
            }
        }

        .text {
            a { @extend %ui-link; }
            padding: $indent-x;
        }

        @include sm {
            padding: $indent-y $indent-x;
        }

    }

</style>



<!--
    Template
-->

<template>
    <div class="t-artwork">

        <a class="image" :style="{paddingTop: image.height / image.width * 100 + '%'}" @click="$emit('click', id)">
            <img class="u-stretch" :src="`${baseURL}/assets/${image.id}`">
        </a>

        <div class="text">
            <p v-if="artist">{{ artist.name }}, {{ artist.lifetime }}</p>
            <p>{{ title }}, {{ year }}</p>
            <p>{{ technique }}</p>
            <p>{{ dimensions }}</p>
            <p>{{ note }}</p>
            <p>Reference No. {{ id }} | <a @click="inquire">Inquire</a></p>
        </div>

    </div>
</template>



<!--
    Scripts
-->

<script>

    export default {

        inheritAttrs: false,

        props: [
            'id',
            'title',
            'artist',
            'image',
            'year',
            'technique',
            'dimensions',
            'note'
        ],

        methods: {

            inquire () {
                const { id, artist, title, year, technique, dimensions, note } = this;
                let inquire = `${title}, ${year}\n${technique} | ${dimensions} | ${note}\nReference No. ${id}`
                if (artist) inquire = `${artist.name}, ${artist.lifetime}\n${inquire}`;
                this.$store.commit('storage/set', ['inquire', inquire]);
            }

        }

    }

</script>
